package com.itheima.redbaby.bean;

import java.io.Serializable;
import java.util.List;

import android.os.Handler;

/**
 * 商品分类的bean
 * @author Crist
 *
 */
public class CommodityClassify implements Serializable{
	private String response;
	private String version;
	private List<ClassifyFist> category;
	public CommodityClassify() {
		
	}
	public CommodityClassify(String response, String version,
			List<ClassifyFist> category) {
		super();
		this.response = response;
		this.version = version;
		this.category = category;
	}
	public String getResponse() {
		return response;
	}
	public void setResponse(String response) {
		this.response = response;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public List<ClassifyFist> getCategory() {
		return category;
	}
	public void setCategory(List<ClassifyFist> category) {
		this.category = category;
	}
	@Override
	public String toString() {
		return "CommodityClassify [response=" + response + ", version="
				+ version + ", category=" + category + "]";
	}
	
}
