package com.itheima.redbaby.bean.order;

import java.io.Serializable;

public class SumUp implements Serializable {
	private String total_count;
	private String total_price;
	private String total_point;
	private String freight;
	private String prom_cut;

	public String getTotal_count() {
		return total_count;
	}

	public void setTotal_count(String total_count) {
		this.total_count = total_count;
	}

	public String getTotal_price() {
		return total_price;
	}

	public void setTotal_price(String total_price) {
		this.total_price = total_price;
	}

	public String getTotal_point() {
		return total_point;
	}

	public void setTotal_point(String total_point) {
		this.total_point = total_point;
	}

	public String getFreight() {
		return freight;
	}

	public void setFreight(String freight) {
		this.freight = freight;
	}

	public String getProm_cut() {
		return prom_cut;
	}

	public void setProm_cut(String prom_cut) {
		this.prom_cut = prom_cut;
	}

}
